     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        if(count(session('customer')) > 0){
            foreach (session('customer') as $key => $value) {
                 $res=reservation::all()->where('cus_id',$value->id);
                 $inv=invoices::all()->where('cus_id',$value->id);
            }
           
            return view('customer.dashboard')
            ->with('booking',$res)
            ->with('inv',$inv);

        }else{
            return redirect('/');   
        }
        
    }

